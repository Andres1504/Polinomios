
public class Nodo {
	private Monomio dato;
	private Nodo siguiente;
	
	public Nodo(){}

	public Monomio getDato() {
		return dato;
	}

	public void setDato(Monomio dato) {
		this.dato = dato;
	}

	public Nodo getSiguiente() {
		return siguiente;
	}

	public void setSiguiente(Nodo siguiente) {
		this.siguiente = siguiente;
	}
	
	
	

}
